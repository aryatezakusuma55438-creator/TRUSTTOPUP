def get_translator(lang):
    """Return a translation function bound to the given language."""
    lang = lang if lang in TRANSLATIONS else 'en'
    table = TRANSLATIONS[lang]
    def t(key):
        return table.get(key, TRANSLATIONS['en'].get(key, key))
    return t
